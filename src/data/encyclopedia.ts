export function getAllMonsters() {
  const all = []
  Object.entries(ENCYCLOPEDIA_DATA.monsters).forEach(([category, monsters]) => {
    Object.entries(monsters).forEach(([name, data]) => {
      all.push({ name, category, ...data })
    })
  })
  return all
}

export function getMonsterByName(name: string) {
  for (const category of Object.values(ENCYCLOPEDIA_DATA.monsters)) {
    if (category[name]) return category[name]
  }
  return null
}

export function getAllMaterials() {
  return Object.entries(ENCYCLOPEDIA_DATA.materials).map(([name, data]) => ({ name, ...data }))
}

export async function getAllFishes() {
  const map = new Map(Object.entries(ENCYCLOPEDIA_DATA.fishes))
  const { getAllFishes: getAllFishesFromData } = await import('./fishing.ts')
  const ALL_FISH = await getAllFishesFromData()
  for (const f of ALL_FISH) {
    if (!map.has(f.name)) {
      map.set(f.name, { lore: f.knowledge || '一种神秘的鱼类。' })
    }
  }
  return Array.from(map.entries()).map(([name, data]) => ({ name, ...data }))
}

export function getAllBooks() {
  return Object.entries(ENCYCLOPEDIA_DATA.books).map(([name, data]) => ({ name, ...data }))
}

export async function getTotalCount(type: string): Promise<number> {
  switch (type) {
    case 'monsters':
      return getAllMonsters().length
    case 'materials':
      return getAllMaterials().length
    case 'fishes':
      return (await getAllFishes()).length
    case 'books':
      return getAllBooks().length
    default:
      return 0
  }
}
